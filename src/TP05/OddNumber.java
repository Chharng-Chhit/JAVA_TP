package src.TP05;

public class OddNumber {
      public static void main(String[] args) {
            System.out.println("Progame in Java to display odd numbers located between 0 and 500");
            System.out.print("Odd Numbers: ");
            for(int i=1; i<=500; i+=2){
                  System.out.print(" "+ i);
            }
      }
}
